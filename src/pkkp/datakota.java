    private void cmdSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cmdSimpanMouseClicked
        String tNama = txtKota.getText();
        int tId = Integer.parseInt(txtId.getText());
        try {
        if (edit==true) {
            stm.executeUpdate("update data_kota set nama_kota='" + tNama + "'where id =" + tId);
        } else {
           stm.executeUpdate("INSERT into data_kota (id, nama_kota) VALUES(" + tId + ",'" + tNama + "')");
        }
            tblKota.setModel(new DefaultTableModel(dataTable, header));
            baca_data();
            aktif(false);
            setTombol(true);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_cmdSimpanMouseClicked

