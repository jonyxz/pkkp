    //method baca data dari Mysql dimasukkan ke table pada form
    private void baca_data() {
        try {
            stm = Con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            RsKota = stm.executeQuery("select * from data_kota");
            ResultSetMetaData meta = RsKota.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            RsKota.last();
            baris = RsKota.getRow(); 
            dataTable = new Object[baris][col];
            int x = 0;
            RsKota.beforeFirst();
            while (RsKota.next()) {
                dataTable[x][0] = RsKota.getString("id");
                dataTable[x][1] = RsKota.getString("nama_kota");
                x++;
            }
            tblKota.setModel(new DefaultTableModel(dataTable, header));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
